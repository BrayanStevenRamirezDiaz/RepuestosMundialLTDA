function consxdocu() {
    let documento = prompt("Ingresa el numero de documento a buscar:");
    let numero = Number(documento);
    let existe = alumnos.find(x=> x.documento === numero );
    
    if (existe){
        alert(`La persona existe es ${existe.nombres} ${existe.apellidos} que vive en ${existe.ciudadnac} con genero ${existe.genero}`);
        
    }
    else{
        alert("La persona no existe");
    }
}

function consxnom() {
    let nombre = prompt("Ingresa el nombre de la persona a buscar:");
    nombre=nombre.toUpperCase();
    let existe = alumnos.find(x=> x.nombres.toUpperCase() === nombre );

    
    if (existe){
        alert(`La persona existe es ${existe.nombres} ${existe.apellidos} que vive en ${existe.ciudadnac} con numero de documento ${existe.documento}`);
        
    }
    else{
        alert("La persona no existe");
    }
}

